const DropdownMenuSubTrigger = React.forwardRef<
  React.ElementRef<typeof DropdownMenuPrimitive.SubTrigger>,
  React.ComponentPropsWithoutRef<typeof DropdownMenuPrimitive.SubTrigger> & {
    inset?: boolean;
  }
/**
 * Renders a sub-trigger component for a dropdown menu with customizable styles and behavior
 * @param {Object} props - The component props
 * @param {string} [props.className] - Additional CSS class names for styling
 * @param {boolean} [props.inset] - Whether to apply inset padding to the left
 * @param {React.ReactNode} props.children - The content to be rendered inside the sub-trigger
 * @param {React.Ref} ref - Ref object for the sub-trigger element
 * @returns {JSX.Element} A styled and configurable dropdown menu sub-trigger component
 */
>(({ className, inset, children, ...props }, ref) => (
  <DropdownMenuPrimitive.SubTrigger
    ref={ref}
    className={cn(
      "flex cursor-default select-none items-center rounded-sm px-2 py-1.5 text-sm outline-none focus:bg-accent data-[state=open]:bg-accent",
      inset && "pl-8",
      className
    )}
    {...props}
  >
    {children}
    <ChevronRightIcon className="ml-auto h-4 w-4" />
  </DropdownMenuPrimitive.SubTrigger>
));
DropdownMenuSubTrigger.displayName =
  DropdownMenuPrimitive.SubTrigger.displayName;

