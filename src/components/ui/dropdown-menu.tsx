/**
 * Renders a shortcut span element for a dropdown menu item
 * @param {React.HTMLAttributes<HTMLSpanElement>} props - The HTML attributes for the span element
 * @param {string} [className] - Additional CSS classes to apply to the span element
 * @returns {JSX.Element} A span element with the provided className and other props
 */
const DropdownMenuShortcut = ({
  className,
  ...props
}: React.HTMLAttributes<HTMLSpanElement>) => {
  return (
    <span
      className={cn("ml-auto text-xs tracking-widest opacity-60", className)}
      {...props}
    />
  );
};
DropdownMenuShortcut.displayName = "DropdownMenuShortcut";

